# 2.Assignment Operator=> 	= ,		+= ,	-= ,	*= ,	/= ,	**= ,	//=

a,b=20,10
print('a=%d\nb=%d'%(a,b))

a,b=20,10
print('a=%d\nb=%d'%(a,b))
c=a+b   				 #use =
print('(=)add=',c)

a,b=20,10
print('a=%d\nb=%d'%(a,b))
a+=b					 #use +=		a=a+b
print('(+=)add= ',a)

a,b=20,10
print('a=%d\nb=%d'%(a,b))
a-=b					 #use -=		a=a-b
print('(-=)sub= ',a)

a,b=20,10
print('a=%d\nb=%d'%(a,b))
a*=b					 #use *=		a=a*b
print('(*=)mul= ',a)

a,b=20,10
print('a=%d\nb=%d'%(a,b))
a/=b					 #use /=		a=a/b
print('(/=)div= ',a)

a,b=20,10
print('a=%d\nb=%d'%(a,b))
a**=b					 #use **=		a=a**b
print('(**=)expo= ',a)

a,b=20,10
print('a=%d\nb=%d'%(a,b))
a//=b					 #use //=		a=a//b
print('(//=)floor= ',a)